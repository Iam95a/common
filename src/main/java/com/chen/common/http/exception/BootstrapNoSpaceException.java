package com.chen.common.http.exception;

public class BootstrapNoSpaceException extends BootstrapException {

  public BootstrapNoSpaceException() {
    super();
  }

  public BootstrapNoSpaceException(String message) {
    super(message);
  }

  public BootstrapNoSpaceException(String message, Throwable cause) {
    super(message, cause);
  }

  public BootstrapNoSpaceException(Throwable cause) {
    super(cause);
  }
}
